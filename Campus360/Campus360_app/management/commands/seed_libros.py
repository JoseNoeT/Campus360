class Command(BaseCommand):
    help = "Inserta 20 libros de prueba en la base de datos."

    def handle(self, *args, **kwargs):
        creados = []
        existentes = []
        for data in LIBROS:
            libro, created = Libro.objects.get_or_create(isbn=data["isbn"], defaults=data)
            if created:
                creados.append(libro.titulo)
            else:
                existentes.append(libro.titulo)
        self.stdout.write(self.style.SUCCESS(f"Libros creados: {len(creados)}"))
        for t in creados:
            self.stdout.write(self.style.SUCCESS(f"  + {t}"))
        self.stdout.write(self.style.WARNING(f"Libros ya existentes: {len(existentes)}"))
        for t in existentes:
            self.stdout.write(self.style.WARNING(f"  - {t}"))
